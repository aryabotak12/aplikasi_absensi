    private void simpan_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpan_btnActionPerformed
        String pass_lama = new String(passLama_pf.getPassword());
        String pass_baru = new String(passBaru_pf.getPassword());
        String konfr_pass = new String(konfrPass_pf.getPassword());
        
        Connection connection = koneksi_to_db.getConnection();
        if (connection != null){
            String get_oldPass = "SELECT password FROM info_login_mhs WHERE uid = ?";
            try(PreparedStatement state = connection.prepareStatement(get_oldPass)){
                state.setString(1, uid);
                ResultSet rs = state.executeQuery();
                if (rs.next()){
                    String password = rs.getString("password");
                    if (pass_lama.equals(password)){
                        if (konfr_pass.equals(pass_baru)){
                            if(connection != null) {
                                String ubah_pass = "UPDATE info_login_mhs SET password = ? WHERE uid = ?";
                                try (PreparedStatement statee = connection.prepareStatement(ubah_pass)){
                                    statee.setString(1, konfr_pass);
                                    statee.setString(2, uid);
                                    statee.executeUpdate();
                                    JOptionPane.showMessageDialog(this, "Password Berhasil diubah!");
                                    new mahasiswa.profil_mhs().setVisible(true);
                                    this.dispose();
                                } catch (Exception e) {
                                    System.out.println("Query Error");
                                }
                            }
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Password Lama Salah!");
                    }
                }
            } catch (SQLException e){
                System.out.println("Query Error");
            }
        }
    }//GEN-LAST:event_simpan_btnActionPerformed

